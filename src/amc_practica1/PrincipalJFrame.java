    private void MostrarGraficaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarGraficaButtonActionPerformed
        
        Graphics g = canvas2.getGraphics();
        int anchuraCanvas = canvas2.getWidth();
        int alturaCanvas = canvas2.getHeight();
        
        long maxY = 0;
        
        int [][] CoordenadasPantalla = new int [metodos.length][tallas.length];
        
        //Primero debemos traducir los puntos a las coordenadas en el lienzo
        //Calculamos el maximo Y para la leyenda del eje de abcisas
        //Por cada metodo dibujamos su correspondiente grÃ¡fica

        for(int i = 0 ; i < metodos.length ; i++)
            for(int x = 0 ; x < tiempos_medios[i].length ; x++)
                if(maxY < tiempos_medios[i][x])
                    maxY = tiempos_medios[i][x];

        
        for(int i = 0 ; i < metodos.length ; i++){
            
            CoordenadasPantalla = LDLA.ConvertirCoordenadasPantalla(tiempos_medios[i] , tallas , anchuraCanvas-30, alturaCanvas-30 , maxY);

            //Dibujamos las lineas
            for (int j = 0; j < CoordenadasPantalla.length-1; j++) {
             
                g.setColor(Color.BLUE);
                g.drawLine(CoordenadasPantalla[0][j], CoordenadasPantalla[1][j], CoordenadasPantalla[0][j+1], CoordenadasPantalla[1][j+1]);            
            }
            
            //Dibujamos los puntos            
            for(int k = 0; k < tiempos_medios[i].length ; k++){
                
                g.setColor(Color.RED);
                g.fillOval(CoordenadasPantalla[0][k]-2,CoordenadasPantalla[1][k]-2, 5, 5);
            
            }
        
        }
        
        // Representamos un rectangulo alrededor de las graficas
        
        int [] xPoints = { 10 , 10 , anchuraCanvas-10 , anchuraCanvas-10 };
        int [] yPoints = { 10 , alturaCanvas-10, alturaCanvas-10 , 10 };
        
        g.setColor(Color.BLACK);
        g.drawPolygon( xPoints , yPoints , 4);
        
        
        // Representamos la leyenda
        for(int i = 0; i<tallas.length; i++){
            g.setColor( Color.magenta );
            g.drawString( String.valueOf(tallas[i]) , CoordenadasPantalla[0][i] , alturaCanvas-2);

        }
        
        g.drawString( String.valueOf(maxY) , 2 , 10 );
        g.drawString( String.valueOf(Math.round(maxY/2)) , 2 , Math.round(alturaCanvas/2) );
        g.drawString( String.valueOf(Math.round(maxY/4)) , 2 , Math.round(3*alturaCanvas/4) );
        g.drawString( String.valueOf(Math.round(3*maxY/4)) , 2 , Math.round(alturaCanvas/4) );
        
    }//GEN-LAST:event_MostrarGraficaButtonActionPerformed

